function isTabActive(pathname: string, href: string, suffix: string): boolean {
  // For the overview tab (no suffix), it must be an exact match
  if (suffix === '') {
    return pathname === href;
  }
  // For other tabs, match if pathname starts with the tab href
  return pathname === href || pathname.startsWith(href + '/');
}

export function ProjectNav({ projectId }: ProjectNavProps) {
  const pathname = usePathname();
  const basePath = `/projects/${projectId}`;

  return (
    <nav aria-label="Project navigation" className="flex gap-1 mb-8 border-b pb-4 overflow-x-auto">
      {navItems.map((item) => {
        const href = `${basePath}${item.suffix}`;
        const active = isTabActive(pathname, href, item.suffix);

        return (
          <Link
            key={href}
            href={href}
            aria-current={active ? 'page' : undefined}
            className={`flex items-center gap-1.5 rounded-lg px-3 py-2 text-sm font-medium transition-all duration-200 hover:text-foreground hover:bg-accent hover:shadow-sm whitespace-nowrap ${
              active
                ? 'bg-accent text-foreground font-semibold shadow-sm'
                : 'text-muted-foreground'
            }`}
          >
            <svg
              className={`w-3.5 h-3.5 ${active ? 'text-foreground' : ''}`}
              fill="none"
              stroke="currentColor"
              viewBox="0 0 24 24"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={2}
                d={item.icon}
              />
            </svg>
            {item.label}
          </Link>
        );
      })}
    </nav>
  );
}
